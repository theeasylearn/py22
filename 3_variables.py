#concept of variables in python 
'''
    syntax 
    variable-name = value 
'''
name = "The easylearn academy"
year = 2015
pi = 3.14
gender = True

print("name = ",name," year = ",year," pi = ",pi," gender = ",gender)

#change variable's value
name = 100
