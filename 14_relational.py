#example of relational operators
num1 = input("enter value for num1")
num2 = input("enter value for num2")

#convert input into int 
num1 = int(num1) # 10
num2 = int(num2) # 20

# //relational expression use relational operators < > == != <= >=
print("num1 == num2",num1 == num2) # false
print("num1 != num2",num1 != num2) # true
print("num1 < num2",num1 < num2) # true
print("num1 > num2",num1 > num2) # false 
print("num1 <= num2",num1 <= num2) # true 
print("num1 >= num2",num1 >= num2) # false
