#example of athematic operators 
num1 = input("enter value for num1")
num2 = input("enter value for num2")

#convert input into int 
num1 = int(num1)
num2 = int(num2)

addition = num1 + num2
subtraction = num1 - num2
multiplication = num1 * num2 
division = num1 / num2 
reminder = num1 % num2 
floor = num1 // num2 # 10^3
power = num1 ** num2 
print(addition,subtraction,multiplication,division,reminder,floor,power)
